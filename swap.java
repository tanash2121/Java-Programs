import java.util.*;

class swap{

public static void main(String [] args){

int x = 100;
int y = 200;

System.out.println("Before the Swapping X =" + x);

System.out.println("Before the Swapping Y =" + y);

x=x+y;//100+200=300
y=x-y;//300-200=100
x=x-y;//300-100=200

System.out.println("After the Swapping X =" + x);


System.out.println("After the Swapping Y =" + y);

	}

} 
